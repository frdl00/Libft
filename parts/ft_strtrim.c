#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*copy;
	size_t	start;
	size_t	end;

	start = 0;
	end = ft_strlen(s1);
	if (!s1 || !set)
		return (NULL);
	while (s1[start] && ft_strchr(set, s1[start]))
	{
		start++;
	}
	while (end > start && ft_strchr(set, s1[end - 1]))
	{
		end--;
	}
	copy = malloc(end - start + 1);
	if (!copy)
		return (NULL);
	ft_memcpy(copy, s1 + start, end - start);
	copy[end - start] = '\0';
	return (copy);
}
